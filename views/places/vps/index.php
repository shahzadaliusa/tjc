                        <h3>Geographical Area</h3>
                        <p>
                            The Corbett National Park is spread in 1318.54 Km<sup>2</sup> where the Core area area is 520 Km<sup>2</sup>
                            which contains the Corbett National Park and Buffer area is 797.72 Km<sup>2</sup> Contains Reserve forest as well as
                            Sonanadi Wildlife Sanctuary.<br/>The Altitute is 360M and 1040M, Lalitude - 29<sup>o</sup>25' and  29<sup>o</sup>39N'
                            Logitude is - 78<sup>o</sup>44' and 79<sup>o</sup>07'E<br/>The Corbett National Park is located between the lesser of
                            Himalayas in north and Shivaliks in south.
                        </p>
