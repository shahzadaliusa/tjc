                        <h3>Zones for Tourism</h3>
                        <p>
                            Although Corbett National Park spread widely yet some zones are reserved for tourism because these zones are
                            considered as high density wildlife areas.<br/><b>Dhikala </b> - Most famous and largest zone of Corbett National
                            Park consist Tourist rest houses for Night Stay of visiters in Jungle and under the security of Government Forest Department.<br/>
                            <b>2. Bijrani</b> - Second Most beautiful and famous zone of Corbett National Park to spot tigers as well as other species.<br/>
                            3. Dhela Forest Zone<br/>4. Durgadevi <br/>5. Sitabani<br/> 6. Jhirna
