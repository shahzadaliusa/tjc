                        <h3>History</h3>
                        <p>
                            Corbett National Park was hunting playground for humans to kill animal in the british government age. Jim Corbett was
                            an animal hunter born in Kumaun. Sir Johns Hewett, Lt Governer of United Provices, at the suggestion of James Corbett
                            decided to establish a wildlife park. On 8<sup>th</sup> August, 1936, the park opened and named as Hailey National Park
                            on the name of Sir Malcom Hailey. Later it changed to Ramganga National Park and finally in 1952, it is named as 
                            Corbett National Park in honour and the memory of Late Jim Corbett.
                        </p>
