                            You need to follow these instructions before booking -<br/>
                            1. Allowed ID Proofs : Driving License, PAN Card, Voter ID, Passport, Student ID etc.<br/>
                            Note : You need to take the same ID Proof on your visit that is mentioned in your Booking Permit.<br/>
                            Note : Booking permit charges are non-refundable.<br/><br/>
                            2. Corbett Tiger Reserve allowed 6 persons maximum in single Gypsy.<br/>
                            3. Night Stay are alloted in first come first serve basis. You need to book your permit for 45 to 90 days 
