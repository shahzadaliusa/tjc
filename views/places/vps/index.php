                        <h3>Diverse Tourism</h3>
                        <p>The tourism is based on visiter's interesets because some of them are bird lover, tree lover, animal lovers and some
                            comes here to spot the tiger. One can get the benefits of all these activities because Corbett National Park is the
                            home for roaring tigers containing large density of animals and tigers.<br/>There are 110 species of Trees, 51 species
                            of Shrubs and over 33 species of bamboo.<br/>There are 50 Mammals species, 580 species of birds and 25 reptile species.
                        </p>
